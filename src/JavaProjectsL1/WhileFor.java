package JavaProjectsL1;

public class WhileFor {
    public static void main(String[] args) {
        int num = 10;
        while (num <= 20) {
            System.out.println("w while ");
            num++;
        }
        for (int i = 0; i <= 10; i++) {
            System.out.println("w for ");

        }

    }
}
