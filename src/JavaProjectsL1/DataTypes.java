package JavaProjectsL1;

public class DataTypes {
    public static void main(String[] args) {
// data types used for integers
        System.out.println(" Byte min : " + Byte.MIN_VALUE + " max : " + Byte.MAX_VALUE + " byte (space used in memory) : " + Byte.SIZE);
        System.out.println(" Short min : " + Short.MIN_VALUE + " max : " + Short.MAX_VALUE + " byte (space used in memory) : " + Short.SIZE);
        System.out.println(" Integer min : " + Integer.MIN_VALUE + "max : " + Integer.MAX_VALUE + " byte (space used in memory) : " + Integer.SIZE);
        System.out.println(" Long min : " + Long.MIN_VALUE + " max : " + Long.MAX_VALUE + " byte (space used in memory) : " + Long.SIZE);

// data types used for decimal numbers
        System.out.println(" Float min : " + Float.MIN_VALUE + " max : " + Float.MAX_VALUE + " byte (space used in memory) : " + Float.SIZE);
        System.out.println(" Double min : " + Double.MIN_VALUE + " max : " + Float.MAX_VALUE + " byte (space used in memory) : " + Double.SIZE);
    }
}

